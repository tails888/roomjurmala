function wrapLiveTextNode(textNode, state) {
  const text = textNode.nodeValue;
  if (!text || !text.trim()) return;
  const frag = document.createDocumentFragment();
  let word = null;

  Array.from(text).forEach((char) => {
    if (/\s/.test(char)) {
      frag.appendChild(document.createTextNode(char));
      word = null;
      return;
    }

    if (!word) {
      word = document.createElement('span');
      word.className = 'write-word';
      frag.appendChild(word);
    }

    const span = document.createElement('span');
    span.className = 'write-char';
    span.textContent = char;
    span.style.setProperty('--char-index', state.index);
    word.appendChild(span);
    state.index += 1;
  });

  textNode.replaceWith(frag);
}

function wrapLiveNode(node, state) {
  Array.from(node.childNodes).forEach((child) => {
    if (child.nodeType === Node.TEXT_NODE) {
      wrapLiveTextNode(child, state);
    } else if (child.nodeType === Node.ELEMENT_NODE && !child.classList.contains('write-char')) {
      wrapLiveNode(child, state);
    }
  });
}

